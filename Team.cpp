#include<bits/stdc++.h>
#define endl '\n'

using namespace std;

int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    int tc, sum = 0;
    cin >> tc;

    while(tc --){
        int a, b, c ;
        cin >> a >> b >> c;

        if( a + b + c >= 2){
            //sum /= sum ;
            sum += 1;


        }


    }
    cout << sum << endl ;





return 0;
}
